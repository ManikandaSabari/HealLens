class I18n {
  constructor() {
    this.currentLang = localStorage.getItem("heallens_lang") || "en";
  }

  setLanguage(lang) {
    this.currentLang = lang;
    localStorage.setItem("heallens_lang", lang);
    this.applyTranslations();
  }

  t(key) {
    if (!TRANSLATIONS[this.currentLang]) return TRANSLATIONS["en"][key] || key;
    return TRANSLATIONS[this.currentLang][key] || TRANSLATIONS["en"][key] || key;
  }

  getVoiceLang() {
    switch (this.currentLang) {
      case "hi": return "hi-IN";
      case "ta": return "ta-IN";
      case "kn": return "kn-IN";
      default: return "en-IN";
    }
  }

  applyTranslations() {
    document.querySelectorAll("[data-i18n]").forEach(el => {
      const key = el.getAttribute("data-i18n");
      const translation = this.t(key);
      if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
        el.placeholder = translation;
      } else {
        el.textContent = translation;
      }
    });
    document.documentElement.lang = this.currentLang;
    
    // Update labels in navigation if needed
    const topbarTitle = document.getElementById("topbar-title");
    if (topbarTitle) {
      const currentTab = document.querySelector(".nav-btn.active")?.getAttribute("data-tab") || "scanner";
      const camelTab = currentTab.split('-').map(word => word.charAt(0).toUpperCase() + word.slice(1)).join('');
      const navKey = "nav" + camelTab;
      topbarTitle.innerText = this.t(navKey);
    }
  }
}

window.i18n = new I18n();

